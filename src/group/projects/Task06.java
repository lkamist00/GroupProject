package group.projects;

public class Task06 {

	public static void main(String[] args) {
		//6. Write a program to swap 2 numbers without a temporary variable?
		
		int a=1;
		int b=6;
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		a=a+b; //1+6=7
		b=a-b; //7-6=1
		a=a-b; //7-1=6
		
		System.out.println("--------------------");
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		
		
		
		
		
		
	}

}
